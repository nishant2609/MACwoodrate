            // HEADER - Company Info + Branding
            // ═══════════════════════════════════
            pw.Container(
              width: double.infinity,
              padding: const pw.EdgeInsets.all(20),
              decoration: pw.BoxDecoration(
                gradient: const pw.LinearGradient(
                  colors: [PdfColors.brown800, PdfColors.brown600],
                ),
                borderRadius: pw.BorderRadius.circular(12),
              ),
              child: pw.Row(
                mainAxisAlignment:
                pw.MainAxisAlignment.spaceBetween,
                crossAxisAlignment: pw.CrossAxisAlignment.start,
                children: [
                  // Company Details
                  pw.Expanded(
                    child: pw.Column(
                      crossAxisAlignment:
                      pw.CrossAxisAlignment.start,
                      children: [
                        pw.Text(
                          companyName,
                          style: pw.TextStyle(
                            fontSize: 24,
                            fontWeight: pw.FontWeight.bold,
                            color: PdfColors.white,
                          ),
                        ),
                        if (ownerName.isNotEmpty) ...[
                          pw.SizedBox(height: 4),
                          pw.Text(
                            ownerName,
                            style: const pw.TextStyle(
                              fontSize: 12,
                              color: PdfColors.white,
                            ),
                          ),
                        ],
                        if (companyPhone.isNotEmpty) ...[
                          pw.SizedBox(height: 4),
                          pw.Text(
                            'Ph: $companyPhone',
                            style: const pw.TextStyle(
                              fontSize: 11,
                              color: PdfColors.white,
                            ),
                          ),
                        ],
                        if (companyAddress.isNotEmpty) ...[
                          pw.SizedBox(height: 4),
                          pw.Text(
                            companyAddress,
                            style: const pw.TextStyle(
                              fontSize: 10,
                              color: PdfColors.white,
                            ),
                          ),
                        ],
                        if (companyGstin.isNotEmpty) ...[
                          pw.SizedBox(height: 4),
                          pw.Text(
                            'GSTIN: $companyGstin',
                            style: const pw.TextStyle(
                              fontSize: 10,
                              color: PdfColors.white,
                            ),
                          ),
                        ],
                      ],
                    ),
                  ),
                  // Document Type Badge
                  pw.Container(
                    padding: const pw.EdgeInsets.symmetric(
                        horizontal: 16, vertical: 8),
                    decoration: pw.BoxDecoration(
                      color: PdfColors.white,
                      borderRadius: pw.BorderRadius.circular(8),
                    ),
                    child: pw.Column(
                      crossAxisAlignment:
                      pw.CrossAxisAlignment.center,
                      children: [
                        pw.Text(
                          'COST ESTIMATE',
                          style: pw.TextStyle(
                            fontSize: 14,
                            fontWeight: pw.FontWeight.bold,
                            color: PdfColors.brown800,
                          ),
                        ),
                        pw.SizedBox(height: 4),
                        pw.Text(
                          'WoodRate Pro',
                          style: pw.TextStyle(
                            fontSize: 10,
                            color: PdfColors.brown600,
                          ),
                        ),
                      ],
                    ),
                  ),
                ],
              ),
            ),

            pw.SizedBox(height: 20),

            // ═══════════════════════════════════
            // ORDER INFO
            // ═══════════════════════════════════
            pw.Container(
              width: double.infinity,
              padding: const pw.EdgeInsets.all(15),
              decoration: pw.BoxDecoration(
                color: PdfColors.grey50,
                borderRadius: pw.BorderRadius.circular(8),
                border:
                pw.Border.all(color: PdfColors.grey300),
              ),
              child: pw.Row(
                crossAxisAlignment: pw.CrossAxisAlignment.start,
                children: [
                  pw.Expanded(
                    flex: 3,
                    child: pw.Column(
                      crossAxisAlignment:
                      pw.CrossAxisAlignment.start,
                      children: [
                        pw.Row(
                          mainAxisAlignment:
                          pw.MainAxisAlignment.spaceBetween,
                          children: [
                            pw.Column(
                              crossAxisAlignment:
                              pw.CrossAxisAlignment.start,
                              children: [
                                pw.Text(
                                  'Order ID: ${order.orderId}',
                                  style: pw.TextStyle(
                                    fontSize: 14,
                                    fontWeight:
                                    pw.FontWeight.bold,
                                  ),
                                ),
                                pw.SizedBox(height: 4),
                                pw.Text(
                                  'Date: ${dateFormat.format(order.date)}',
                                  style: const pw.TextStyle(
                                      fontSize: 11),
                                ),
                              ],
                            ),
                            pw.Container(
                              padding:
                              const pw.EdgeInsets.symmetric(
                                  horizontal: 12,
                                  vertical: 6),
                              decoration: pw.BoxDecoration(
                                color: PdfColors.green100,
                                borderRadius:
                                pw.BorderRadius.circular(12),
                                border: pw.Border.all(
                                    color: PdfColors.green400),
                              ),
                              child: pw.Text(
                                'CONFIRMED',
                                style: pw.TextStyle(
                                  fontSize: 10,
                                  fontWeight: pw.FontWeight.bold,
                                  color: PdfColors.green800,
                                ),
                              ),
                            ),
                          ],
                        ),
                        pw.SizedBox(height: 10),
                        pw.Container(
                          width: double.infinity,
                          padding: const pw.EdgeInsets.all(10),
                          decoration: pw.BoxDecoration(
                            color: PdfColors.blue50,
                            borderRadius:
                            pw.BorderRadius.circular(6),
                            border: pw.Border.all(
                                color: PdfColors.blue200),
                          ),
                          child: pw.Column(
                            crossAxisAlignment:
                            pw.CrossAxisAlignment.start,
                            children: [
                              pw.Text(
                                'Item Description:',
                                style: pw.TextStyle(
                                  fontSize: 10,
                                  fontWeight: pw.FontWeight.bold,
                                  color: PdfColors.blue800,
                                ),
                              ),
                              pw.SizedBox(height: 4),
                              pw.Text(
                                order.itemDescription,
                                style: pw.TextStyle(
                                  fontSize: 13,
                                  fontWeight: pw.FontWeight.bold,
                                  color: PdfColors.blue900,
                                ),
                              ),
                            ],
                          ),
                        ),
                      ],
                    ),
                  ),
                  if (itemImage != null) ...[
                    pw.SizedBox(width: 15),
                    pw.Container(
                      width: 110,
                      height: 110,
                      decoration: pw.BoxDecoration(
                        border: pw.Border.all(
                            color: PdfColors.grey400),
                        borderRadius:
                        pw.BorderRadius.circular(6),
                      ),
                      child: pw.Image(itemImage,
                          fit: pw.BoxFit.cover),
                    ),
                  ],
                ],
              ),
            ),

            pw.SizedBox(height: 20),

            // ═══════════════════════════════════
            // WOOD COMPONENTS TABLE
            // ═══════════════════════════════════
            pw.Text(
              'Wood Components',
              style: pw.TextStyle(
                fontSize: 16,
                fontWeight: pw.FontWeight.bold,
                color: PdfColors.brown800,
              ),
            ),
            pw.SizedBox(height: 8),

            pw.Table(
              border: pw.TableBorder.all(
                  color: PdfColors.grey300, width: 0.5),
              columnWidths: {
                0: const pw.FlexColumnWidth(2),
                1: const pw.FlexColumnWidth(2),
                2: const pw.FlexColumnWidth(1.2),
                3: const pw.FlexColumnWidth(0.8),
                4: const pw.FlexColumnWidth(1),
                5: const pw.FlexColumnWidth(1.2),
              },
              children: [
                // Header
                pw.TableRow(
                  decoration: pw.BoxDecoration(
                      color: PdfColors.brown800),
                  children: [
                    _tableHeader('Wood Type'),
                    _tableHeader('Dimensions'),
                    _tableHeader('Rate/CFT'),
                    _tableHeader('Qty'),
                    _tableHeader('CFT'),
                    _tableHeader('Amount'),
                  ],
                ),
                // Data rows
                ...order.components
                    .asMap()
                    .entries
                    .map((entry) {
                  final i = entry.key;
                  final component = entry.value;
                  final isEven = i % 2 == 0;
                  return pw.TableRow(
                    decoration: pw.BoxDecoration(
                      color: isEven
                          ? PdfColors.white
                          : PdfColors.grey50,
                    ),
                    children: [
                      _tableCell(component.woodType),
                      _tableCell(component.formattedSize),
                      _tableCell(
                          formatCurrency(component.ratePerCft)),
                      _tableCell(
                          component.quantity.toString()),
                      _tableCell(
                          component.cft.toStringAsFixed(2)),
                      _tableCell(
                        formatCurrency(component.totalCost),
                        bold: true,
                      ),
                    ],
                  );
                }).toList(),
                // Subtotal row
                pw.TableRow(
                  decoration: const pw.BoxDecoration(
                      color: PdfColors.brown100),
                  children: [
                    pw.Padding(
                      padding: const pw.EdgeInsets.all(6),
                      child: pw.Text(
                        'Wood Subtotal',
                        style: pw.TextStyle(
                          fontWeight: pw.FontWeight.bold,
                          fontSize: 10,
                          color: PdfColors.brown800,
                        ),
                      ),
                    ),
                    pw.Padding(
                        padding: const pw.EdgeInsets.all(6),
                        child: pw.Text('')),
                    pw.Padding(
                        padding: const pw.EdgeInsets.all(6),
                        child: pw.Text('')),
                    pw.Padding(
                        padding: const pw.EdgeInsets.all(6),
                        child: pw.Text('')),
                    pw.Padding(
                      padding: const pw.EdgeInsets.all(6),
                      child: pw.Text(
                        order.totalCft.toStringAsFixed(2),
                        style: pw.TextStyle(
                          fontWeight: pw.FontWeight.bold,
                          fontSize: 10,
                          color: PdfColors.brown800,
                        ),
                      ),
                    ),
                    pw.Padding(
                      padding: const pw.EdgeInsets.all(6),
                      child: pw.Text(
                        formatCurrency(woodSubtotal),
                        style: pw.TextStyle(
                          fontWeight: pw.FontWeight.bold,
                          fontSize: 10,
                          color: PdfColors.brown800,
                        ),
                      ),
                    ),
                  ],
                ),
              ],
            ),

            pw.SizedBox(height: 20),

            // ═══════════════════════════════════
            // COST SUMMARY
            // ═══════════════════════════════════
            pw.Text(
              'Cost Summary',
              style: pw.TextStyle(
                fontSize: 16,
                fontWeight: pw.FontWeight.bold,
                color: PdfColors.brown800,
              ),
            ),
            pw.SizedBox(height: 8),

            pw.Container(
              width: double.infinity,
              decoration: pw.BoxDecoration(
                border:
                pw.Border.all(color: PdfColors.grey300),
                borderRadius: pw.BorderRadius.circular(8),
              ),
              child: pw.Column(
                children: [
                  _summaryRow(
                      'Wood Components', woodSubtotal, formatCurrency),
                  _summaryDivider(),
                  _summaryRow(
                      'Labour Charges', order.labour, formatCurrency),
                  _summaryRow(
                      'Hardware Costs', order.hardware, formatCurrency),
                  _summaryRow('Factory Overheads', order.factory,
                      formatCurrency),
                  if (order.additionalCharges.isNotEmpty) ...[
                    _summaryDivider(),
                    pw.Container(
                      width: double.infinity,
                      padding: const pw.EdgeInsets.all(8),
                      color: PdfColors.orange50,
                      child: pw.Text(
                        'Other Charges',
                        style: pw.TextStyle(
                          fontWeight: pw.FontWeight.bold,
                          fontSize: 11,
                          color: PdfColors.orange800,
                        ),
                      ),
                    ),
                    ...order.additionalCharges
                        .map((charge) => _summaryRow(
                        charge['name'] as String,
                        charge['amount'] as double,
                        formatCurrency))
                        .toList(),
                    _summaryRow(
                      'Additional Total',
                      order.additionalChargesTotal,
                      formatCurrency,
                      isBold: true,
                      bgColor: PdfColors.orange100,
                    ),
                  ],
                  _summaryDivider(),
                  _summaryRow('Subtotal Before Profit',
                      subtotalBeforeProfit, formatCurrency,
                      isBold: true),
                  _summaryRow(
                      'Profit (${profitPercentage.toStringAsFixed(1)}%)',
                      order.profit,
                      formatCurrency),
                  // Total row
                  pw.Container(
                    width: double.infinity,
                    padding: const pw.EdgeInsets.all(12),
                    decoration: const pw.BoxDecoration(
                      color: PdfColors.brown800,
                      borderRadius: pw.BorderRadius.only(
                        bottomLeft: pw.Radius.circular(8),
                        bottomRight: pw.Radius.circular(8),
                      ),
                    ),
                    child: pw.Row(
                      mainAxisAlignment:
                      pw.MainAxisAlignment.spaceBetween,
                      children: [
                        pw.Text(
                          'TOTAL ESTIMATED COST',
                          style: pw.TextStyle(
                            fontWeight: pw.FontWeight.bold,
                            fontSize: 14,
                            color: PdfColors.white,
                          ),
                        ),
                        pw.Text(
                          formatCurrency(order.total),
                          style: pw.TextStyle(
                            fontWeight: pw.FontWeight.bold,
                            fontSize: 16,
                            color: PdfColors.yellow,
                          ),
                        ),
                      ],
                    ),
