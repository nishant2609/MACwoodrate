    return pdf;
  }

  // Helper widgets
  static pw.Widget _tableHeader(String text) {
    return pw.Padding(
      padding: const pw.EdgeInsets.all(6),
      child: pw.Text(
        text,
        style: pw.TextStyle(
          fontWeight: pw.FontWeight.bold,
          fontSize: 9,
          color: PdfColors.white,
        ),
      ),
    );
  }

  static pw.Widget _tableCell(String text, {bool bold = false}) {
    return pw.Padding(
      padding: const pw.EdgeInsets.all(6),
      child: pw.Text(
        text,
        style: pw.TextStyle(
          fontSize: 9,
          fontWeight:
          bold ? pw.FontWeight.bold : pw.FontWeight.normal,
        ),
      ),
    );
  }

  static pw.Widget _summaryRow(
      String label,
      double amount,
      String Function(double) formatCurrency, {
        bool isBold = false,
        PdfColor? bgColor,
      }) {
    return pw.Container(
      color: bgColor,
      padding: const pw.EdgeInsets.symmetric(
          horizontal: 12, vertical: 7),
      child: pw.Row(
        mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
        children: [
          pw.Text(
            label,
            style: pw.TextStyle(
              fontSize: isBold ? 12 : 11,
              fontWeight: isBold
                  ? pw.FontWeight.bold
                  : pw.FontWeight.normal,
            ),
          ),
          pw.Text(
            formatCurrency(amount),
            style: pw.TextStyle(
              fontSize: isBold ? 12 : 11,
              fontWeight: isBold
                  ? pw.FontWeight.bold
                  : pw.FontWeight.normal,
            ),
          ),
        ],
      ),
    );
  }

  static pw.Widget _summaryDivider() {
    return pw.Container(
      height: 0.5,
      color: PdfColors.grey300,
    );
  }
}