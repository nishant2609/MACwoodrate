    if (order.itemImagePath != null) {
      try {
        final imageFile = File(order.itemImagePath!);
        if (await imageFile.exists()) {
          final imageBytes = await imageFile.readAsBytes();
          itemImage = pw.MemoryImage(imageBytes);
        }
      } catch (e) {
        itemImage = null;
      }
